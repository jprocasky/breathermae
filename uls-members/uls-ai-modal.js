jQuery(function ($) {

  function openModal() {
    $('#uls-ai-modal').fadeIn(150);
    
  }

  function closeModal() {
    $('#uls-ai-modal').fadeOut(150);
  }

  

  // Close handlers
  $(document).on('click', '.uls-ai-modal-close, .uls-ai-modal-overlay', closeModal);

    // ✅ Copy AI Summary text to clipboard
    $(document).on('click', '.uls-ai-copy', function () {
    const text = $('#uls-ai-modal .uls-ai-modal-body').text().trim();

    if (!text) {
        alert('Nothing to copy.');
        return;
    }

    if (!navigator.clipboard) {
        alert('Clipboard access is not available in this browser.');
        return;
    }

    navigator.clipboard.writeText(text)
        .then(function () {
        alert('AI summary copied to clipboard.');
        })
        .catch(function () {
        alert('Failed to copy AI summary.');
        });
    });

  // View AI Summary click
  $(document).on('click', '.uls-ai-view', function (e) {
    e.preventDefault();

    const fileId = $(this).data('id');
    const context = $(this)
    .closest('.uls-files-panel')
    .data('context');

    const $modal = $('#uls-ai-modal');

    // Reset modal state
    $modal.find('.uls-ai-modal-body').html(
      '<p class="uls-ai-loading">Loading summary…</p>'
    );
    $modal.find('.uls-ai-modal-meta').empty();

    openModal();

        // ✅ Inject Copy Text button into modal header (once)
        const $header = $modal.find('.uls-ai-modal-header');

        if ($header.find('.uls-ai-copy').length === 0) {
        $('<button>', {
            type: 'button',
            class: 'uls-ai-copy',
            text: 'Copy Text',
            title: 'Copy AI summary to clipboard'
        }).insertBefore($header.find('.uls-ai-modal-close'));
        }


        $.ajax({
        url: ULS_AI.ajaxurl,
        method: 'POST',
        dataType: 'json',
        data: {
            action: 'uls_get_ai_file_summary',
            nonce:  ULS_AI.nonce,
            file_id: fileId,
            context: context
        }
        })
        .done(function (resp) {
        if (!resp || !resp.success) {
            $modal.find('.uls-ai-modal-body').html(
            '<p>Unable to load summary.</p>'
            );
            return;
        }

        const d = resp.data;

        const meta = `
        <div class="uls-ai-meta-main">
            Generated on ${d.updated_at || d.created_at}
            ${d.document_type_label ? ` · AI Prompt Type: ${escapeHtml(d.document_type_label)}` : ''}
            · Context: ${escapeHtml(d.context)}
            · Model: ${escapeHtml(d.model_used)}
        </div>

        <div class="uls-ai-meta-disclaimer">
            <strong>AI Notice:</strong> This summary is generated by artificial intelligence and may
            contain errors or omissions. It should be used as a convenience reference only and
            not relied upon as a complete, authoritative, or definitive representation of the
            original document.
        </div>
        `;

        $modal.find('.uls-ai-modal-meta').html(meta);

        // ✅ LAST-RESORT SAFETY FILTER: suppress reaction level headers
        let filteredSummaryText = d.summary_text;

        // Remove any lines or line fragments containing reaction/severity headers
        filteredSummaryText = filteredSummaryText.replace(
        /(^|\n).*?\b(Food\s+Reaction\s+Levels?|Reaction\s+Levels?|[234]\+\s*Reactions?|Severe|Moderate|High)\b.*?(?=\n|$)/gim,
        ''
        );

        // Remove leftover bullet-only severity labels
        filteredSummaryText = filteredSummaryText.replace(
        /^\s*[-•]?\s*(Severe|Moderate|High)\s*:?$/gim,
        ''
        );

        // Collapse multiple blank lines created by removals
        filteredSummaryText = filteredSummaryText.replace(/\n{3,}/g, '\n\n').trim();   

        renderSummary(
            $modal.find('.uls-ai-modal-body'),
            filteredSummaryText
        );
        })
/*         .fail(function (jqXHR) {
        $modal.find('.uls-ai-modal-body').html(
            `<p>Unexpected error loading summary.<br>Status: ${jqXHR.status}</p>`
        );
        }); */
        .fail(function (jqXHR) {
        let msg = 'Unexpected error loading summary.';

        if (jqXHR.responseJSON && jqXHR.responseJSON.data && jqXHR.responseJSON.data.message) {
            msg += '\n' + jqXHR.responseJSON.data.message;
        } else if (jqXHR.responseText) {
            msg += '\n' + jqXHR.responseText;
        }

        $modal.find('.uls-ai-modal-body').html(
            `<pre>${escapeHtml(msg)}</pre>`
        );

        
        });
  });

  // Simple formatter (can be enhanced later)
    function renderSummary($container, rawText) {

        const text = normalizeSummaryText(rawText);

        if (!text) {
            $container.html('<p>No summary available.</p>');
            return;
        }

        const sections = text.split(/\n\s*\n/);
        let html = '';

        sections.forEach(section => {

            // Bullet section
            if (section.match(/^[-•]/m)) {
            const items = section.split('\n')
                .filter(l => l.trim())
                .map(l => `<li>${escapeHtml(l.replace(/^[-•]\s*/, ''))}</li>`)
                .join('');

            html += `<ul>${items}</ul>`;
            } 
            else {
            html += `<p class="uls-ai-narrative">${escapeHtml(section)}</p>`;
            }
        });

        $container.html(html);
    }

  function escapeHtml(str) {
    return $('<div>').text(str).html();
  }


    function normalizeSummaryText(text) {
    if (!text) return '';

    return text
        // Headings: ### Title → Title
        .replace(/^###\s*/gm, '')

        // Bold: **Text** → Text
        .replace(/\*\*(.*?)\*\*/g, '$1')

        // Horizontal rule markers
        .replace(/^--+$/gm, '')

        // Trim excess whitespace
        .trim();
    }
});